/**
 * Provalk asset pipeline.
 *
 * Source of truth: branding/provalk-logo.jpeg (1024x1024, no alpha)
 *
 * Generates:
 *   - Android launcher icons (all 5 mipmap densities, square + round)
 *   - Android adaptive icon foreground (Android 8+) — handled by mipmap-anydpi-v26
 *   - iOS AppIcon.appiconset (all required sizes + Contents.json)
 *   - In-app PNG (256x256) at src/assets/logo.png
 *
 * Run:  node scripts/generate-app-assets.js
 *
 * Idempotent: rerunnable without side effects. All output paths are overwritten.
 *
 * Notes:
 *   - iOS 1024x1024 must NOT have an alpha channel (App Store reject rule).
 *   - Android round icons are generated by masking the square version with a circle.
 *   - The "round" launcher icon is what MIUI / Pixel / Samsung One UI all display
 *     when the user has a circular icon mask preference.
 */
const fs   = require('fs');
const path = require('path');
const sharp = require('sharp');

// ── Config ───────────────────────────────────────────────────────────────────
const ROOT      = path.resolve(__dirname, '..');
const SRC       = path.join(ROOT, 'branding', 'provalk-logo.jpeg');
const ANDROID   = path.join(ROOT, 'android', 'app', 'src', 'main', 'res');
const IOS_ICON  = path.join(ROOT, 'ios', 'SecurBookingApp', 'Images.xcassets', 'AppIcon.appiconset');
const APP_ASSET = path.join(ROOT, 'src', 'assets');

// Android launcher densities → square pixel size
const ANDROID_SIZES = {
  'mipmap-mdpi':    48,
  'mipmap-hdpi':    72,
  'mipmap-xhdpi':   96,
  'mipmap-xxhdpi':  144,
  'mipmap-xxxhdpi': 192,
};

// iOS AppIcon required sizes (per Apple's icon spec for iOS 14+).
// Each entry is { size, scale, idiom, filename }.
const IOS_ICONS = [
  // iPhone
  { size: 20, scale: 2, idiom: 'iphone', filename: 'icon-20@2x.png' },
  { size: 20, scale: 3, idiom: 'iphone', filename: 'icon-20@3x.png' },
  { size: 29, scale: 2, idiom: 'iphone', filename: 'icon-29@2x.png' },
  { size: 29, scale: 3, idiom: 'iphone', filename: 'icon-29@3x.png' },
  { size: 40, scale: 2, idiom: 'iphone', filename: 'icon-40@2x.png' },
  { size: 40, scale: 3, idiom: 'iphone', filename: 'icon-40@3x.png' },
  { size: 60, scale: 2, idiom: 'iphone', filename: 'icon-60@2x.png' },
  { size: 60, scale: 3, idiom: 'iphone', filename: 'icon-60@3x.png' },
  // iPad
  { size: 20,   scale: 1, idiom: 'ipad', filename: 'icon-20.png' },
  { size: 20,   scale: 2, idiom: 'ipad', filename: 'icon-20@2x-ipad.png' },
  { size: 29,   scale: 1, idiom: 'ipad', filename: 'icon-29.png' },
  { size: 29,   scale: 2, idiom: 'ipad', filename: 'icon-29@2x-ipad.png' },
  { size: 40,   scale: 1, idiom: 'ipad', filename: 'icon-40.png' },
  { size: 40,   scale: 2, idiom: 'ipad', filename: 'icon-40@2x-ipad.png' },
  { size: 76,   scale: 1, idiom: 'ipad', filename: 'icon-76.png' },
  { size: 76,   scale: 2, idiom: 'ipad', filename: 'icon-76@2x.png' },
  { size: 83.5, scale: 2, idiom: 'ipad', filename: 'icon-83.5@2x.png' },
  // App Store marketing
  { size: 1024, scale: 1, idiom: 'ios-marketing', filename: 'icon-1024.png' },
];

// ── Helpers ──────────────────────────────────────────────────────────────────
function ensureDir(d) {
  if (!fs.existsSync(d)) fs.mkdirSync(d, { recursive: true });
}

/**
 * Build a circular SVG mask of the given size — used to produce "round" Android icons.
 */
function circularMask(size) {
  return Buffer.from(
    `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}">
       <circle cx="${size / 2}" cy="${size / 2}" r="${size / 2}" fill="white"/>
     </svg>`,
  );
}

/**
 * Chroma-keys the dark navy background (#061a32) of the source JPEG, recolors
 * every visible pixel to brand gold, and writes a transparent PNG cutout of
 * the eagle. This is what the login hero displays — no ring, no card, just
 * the mark.
 *
 * Why recolor instead of preserving original tones:
 *   - JPEG quantization leaves ~3-5% color shift along edges. Recoloring to a
 *     uniform brand gold removes that artifact and gives a clean engraved look.
 *   - The original logo is monochrome gold-on-navy, so recoloring loses no
 *     intentional design information.
 */
async function generateLogoMark(outPath) {
  const { data, info } = await sharp(SRC).raw().toBuffer({ resolveWithObject: true });
  const w = info.width, h = info.height, c = info.channels;

  const bg = [6, 26, 50];                    // #061a32 — source background
  const goldR = 241, goldG = 196, goldB = 125; // palette.goldEnd

  const out = Buffer.alloc(w * h * 4);
  for (let p = 0, q = 0; p < data.length; p += c, q += 4) {
    const r = data[p], g = data[p + 1], b = data[p + 2];
    const dr = r - bg[0], dg = g - bg[1], db = b - bg[2];
    const dist = Math.sqrt(dr * dr + dg * dg + db * db);

    // Soft alpha mask: hard transparent < 12, soft ramp 12..35, opaque > 35.
    let alpha;
    if (dist < 12)      alpha = 0;
    else if (dist < 35) alpha = Math.round(((dist - 12) / 23) * 255);
    else                alpha = 255;

    // Recolor visible pixels to brand gold, blending toward dark gold for
    // mid-luminance pixels so detail (line work, etched grooves) is preserved.
    const lum = 0.299 * r + 0.587 * g + 0.114 * b;
    const k = Math.min(1, lum / 180);
    out[q]     = Math.round(goldR * k + 70 * (1 - k));
    out[q + 1] = Math.round(goldG * k + 50 * (1 - k));
    out[q + 2] = Math.round(goldB * k + 25 * (1 - k));
    out[q + 3] = alpha;
  }

  await sharp(out, { raw: { width: w, height: h, channels: 4 } })
    .png()
    .trim()
    .resize(512, 512, { fit: 'contain', background: { r: 0, g: 0, b: 0, alpha: 0 } })
    .toFile(outPath);
}
// ── Pipeline ─────────────────────────────────────────────────────────────────
async function run() {
  if (!fs.existsSync(SRC)) {
    console.error(`✗ Source not found: ${SRC}`);
    process.exit(1);
  }

  const meta = await sharp(SRC).metadata();
  console.log(`✓ Source: ${meta.width}x${meta.height} ${meta.format} (alpha: ${meta.hasAlpha})`);
  if (meta.width !== meta.height) {
    console.warn(`⚠ Source is not square — output will be center-cropped`);
  }

  // ── Android — square + round PNG mipmaps ──────────────────────────────────
  console.log('\n● Android launcher icons');
  for (const [folder, size] of Object.entries(ANDROID_SIZES)) {
    const dir = path.join(ANDROID, folder);
    ensureDir(dir);

    // Square icon
    await sharp(SRC)
      .resize(size, size, { fit: 'cover', position: 'center' })
      .png({ compressionLevel: 9 })
      .toFile(path.join(dir, 'ic_launcher.png'));

    // Round icon (circular mask)
    const buf = await sharp(SRC)
      .resize(size, size, { fit: 'cover', position: 'center' })
      .png()
      .toBuffer();
    await sharp(buf)
      .composite([{ input: circularMask(size), blend: 'dest-in' }])
      .png({ compressionLevel: 9 })
      .toFile(path.join(dir, 'ic_launcher_round.png'));

    console.log(`  ${folder.padEnd(18)} ${size}x${size}`);
  }

  // ── iOS — AppIcon.appiconset ──────────────────────────────────────────────
  console.log('\n● iOS app icons');
  ensureDir(IOS_ICON);

  for (const icon of IOS_ICONS) {
    const px = Math.round(icon.size * icon.scale);
    const out = path.join(IOS_ICON, icon.filename);

    let pipeline = sharp(SRC).resize(px, px, { fit: 'cover', position: 'center' });

    // 1024x1024 must NOT have alpha (App Store rule). Flatten on white if needed.
    // Source is JPEG so already has no alpha — but we keep this defensive.
    if (icon.size === 1024) {
      pipeline = pipeline.flatten({ background: '#0c1220' });
    }

    await pipeline.png({ compressionLevel: 9 }).toFile(out);
    console.log(`  ${icon.filename.padEnd(28)} ${px}x${px}`);
  }

  // Contents.json — Xcode validates this strictly
  const contents = {
    images: IOS_ICONS.map(i => ({
      size:     `${i.size}x${i.size}`,
      idiom:    i.idiom,
      filename: i.filename,
      scale:    `${i.scale}x`,
    })),
    info: { version: 1, author: 'xcode' },
  };
  fs.writeFileSync(path.join(IOS_ICON, 'Contents.json'), JSON.stringify(contents, null, 2));
  console.log('  Contents.json (manifest)');

  // ── In-app logo for login hero ────────────────────────────────────────────
  console.log('\n● In-app assets');
  ensureDir(APP_ASSET);

  // 256x256 base — RN at 3x density will display crisply at 80px target
  await sharp(SRC)
    .resize(256, 256, { fit: 'cover', position: 'center' })
    .png({ compressionLevel: 9 })
    .toFile(path.join(APP_ASSET, 'logo.png'));
  console.log(`  src/assets/logo.png  256x256`);

  // Eagle-only cutout for the login hero — chroma-keys the dark JPEG bg
  // and recolors visible pixels to brand gold. 512x512 trimmed bbox.
  await generateLogoMark(path.join(APP_ASSET, 'logo-mark.png'));
  console.log(`  src/assets/logo-mark.png  512x512 (transparent, brand gold)`);

  console.log('\n✓ All assets generated successfully.\n');
}

run().catch(err => {
  console.error('✗ Asset generation failed:', err);
  process.exit(1);
});
