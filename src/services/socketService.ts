  private eventListeners = new Map<ServerEvent, Set<(payload: unknown) => void>>();

  /** Attach every registered listener to the current socket instance. */
  private bindEventListeners() {
    if (!this.socket) return;
    for (const [event, callbacks] of this.eventListeners) {
      for (const cb of callbacks) {
        this.socket.on(event, cb as (...args: unknown[]) => void);
      }
    }
  }

  /** Register a listener and bind it to the live socket (if any). */
  private addListener<T>(event: ServerEvent, cb: (payload: T) => void): () => void {
    let set = this.eventListeners.get(event);
    if (!set) {
      set = new Set();
      this.eventListeners.set(event, set);
    }
    set.add(cb as (payload: unknown) => void);
    this.socket?.on(event, cb as (...args: unknown[]) => void);

    return () => {
      this.eventListeners.get(event)?.delete(cb as (payload: unknown) => void);
      this.socket?.off(event, cb as (...args: unknown[]) => void);
    };
  }

