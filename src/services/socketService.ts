  joinMission(missionId: string) {
    const prev = this.roomRefCounts.get(missionId) ?? 0;
    this.roomRefCounts.set(missionId, prev + 1);
    // Only emit on the first join for this mission; the server room membership
    // is idempotent but we avoid redundant traffic and keep semantics clear.
    if (prev === 0) {
      this.socket?.emit('join_mission', { missionId });
      if (DEBUG) console.warn(`[WS-Client] join_mission:${missionId} (refs: 1)`);
    } else if (DEBUG) {
      console.warn(`[WS-Client] join_mission:${missionId} (refs: ${prev + 1})`);
    }
  }

  leaveMission(missionId: string) {
    const prev = this.roomRefCounts.get(missionId) ?? 0;
    if (prev <= 0) return; // never joined (or already fully left) ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â no-op

    const next = prev - 1;
    if (next === 0) {
      this.roomRefCounts.delete(missionId);
      this.socket?.emit('leave_mission', { missionId });
      if (DEBUG) console.warn(`[WS-Client] leave_mission:${missionId} (refs: 0)`);
    } else {
      this.roomRefCounts.set(missionId, next);
      if (DEBUG) console.warn(`[WS-Client] leaveMission:${missionId} kept alive (refs: ${next})`);
    }
  }

